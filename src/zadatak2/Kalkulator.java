package zadatak2;

public class Kalkulator {
	
	int a, b;

	public int sabiranje(int a, int b) {

		return a + b;
	}

	public int oduzimanje(int a, int b) {

		return a - b;
	}

	public int mnozenje(int a, int b) {

		return a * b;
	}

	public double dijeljenje(int a, int b) {

		return a / b;
	}

}
